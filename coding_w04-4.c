#include <stdio.h>

int main() {
    // กรณีที่ 1: ใช้ int (ที่ไม่รองรับทศนิยม)
    int score1 = 90.5;  // จะเกิด error เพราะ int เก็บค่าทศนิยมไม่ได้

    // กรณีที่ 2: ใช้ float (รองรับค่าทศนิยม)
    float score2 = 90.5;  // ทำงานได้ถูกต้อง

    // แสดงผลกรณีที่ 2 เท่านั้น
    printf("กรณีที่ 2: score = %.2f\n", score2);  // จะแสดงผล 90.50

    return 0;
}

//ถ้าใช้ int score1 = 90.5; จะเกิด error เพราะ int เก็บแค่จำนวนเต็มไม่ได้ ต้องใช้ float หรือ double แทน ถ้าต้องการเก็บค่าทศนิยม 